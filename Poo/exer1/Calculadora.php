<?php

class Calculadora{

    public function add($x)
    {
        return $x = $this->add($x);
    }
    public function sub($x)
    {
        return $x = $this->sub($x);
    }
    public function multiply($x)
    {
        return $x = $this->multiply($x);
    }
    public function divide($x)
    {
        return $x = $this->divide($x);
    }

}